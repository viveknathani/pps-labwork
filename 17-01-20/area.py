# area of circle, rectangle, triangle, square

print("Circle")
r=float(input("Enter value of radius : "))
area=3.14*r*r
print("Area=", area)

print("Rectangle")
l=float(input("Enter length of rectangle : "))
b=float(input("Enter breadth of rectangle : "))
area=l*b
print("Area=", area)

print("Square")
s=float(input("Enter side length of square : "))
area=s*s
print("Area=", area)

print("Triangle")
b=float(input("Enter base length of triangle : "))
h=float(input("Enter height of triangle : "))
area=b*h*0.5
print("Area=", area)
